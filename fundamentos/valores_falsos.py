# Valores que son considerados falsos en Python

# False
print(False)  # Imprime False

# None
print(None)  # Imprime None

# Cero
print(0)  # Imprime 0

# Cero flotante
print(0.0)  # Imprime 0.0

# Cadenas vacías con doble comilla o comilla simple
print("")  # Imprime ""

# Diccionarios vacíos
print({})  # Imprime {}

# Tuplas vacías
print(())  # Imprime ()

# Listas vacías
print([])  # Imprime []